class Operators{
	public static void main(String args[]){
		int a = 10;
		int b = 5;
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);
		System.out.println(a<=b);
		boolean x=false, y=true;
		System.out.println(x && y);
		System.out.println(x || y);
		System.out.println(!x);
		
		}
	}

